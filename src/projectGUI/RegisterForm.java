    private boolean validate_reg(String memberID) {
        try{           
            pStatmt = myConn.prepareStatement("Select * from membership where memberID=?");
            pStatmt.setString(1, memberID); 
            ResultSet myRs = pStatmt.executeQuery();                        
            if(myRs.next()) {           
                return true;   
            }
            else{
                return false; 
            }           
        }catch(Exception e){
            e.printStackTrace();
            return false;
        }       
    }
