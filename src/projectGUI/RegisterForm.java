    private void memberRegBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_memberRegBtnActionPerformed
        String SQL = "INSERT INTO membership VALUES (?,?,?,?,?)";

        String memberID = memberIDTxtFld.getText();
        String name = memberNameTxtFld.getText();
        String password = memberPasswrdTxtFld.getText();
        String phone = memberPhoneTxtFld.getText();
        String email = memberEmailTxtFld.getText();
        
        if(validate_reg(memberID)){
            JOptionPane.showMessageDialog(null, "Existing user! Please enter another Member ID.");
            //memberIDTxtFld.setText("");
            clearInput();
            this.setVisible(false);
            this.setVisible(true);
        }

        try{
            pStatmt = myConn.prepareStatement(SQL);

            myStatmt = myConn.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE,ResultSet.CONCUR_UPDATABLE);

            if(memberID.isEmpty() || name.isEmpty() || password.isEmpty() || phone.isEmpty() || email.isEmpty()){//any data not filled
                JOptionPane.showMessageDialog(null, "Please fill in all the fields!!", "Alert", JOptionPane.WARNING_MESSAGE);

            }else{
                pStatmt.setString(1, memberIDTxtFld.getText());
                pStatmt.setString(2, memberNameTxtFld.getText());
                pStatmt.setString(3, memberPasswrdTxtFld.getText());
                pStatmt.setString(4, memberPhoneTxtFld.getText());
                pStatmt.setString(5, memberEmailTxtFld.getText());
                pStatmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Registered successfully! You will now return to Login page.");
                LoginForm frame = new LoginForm();
                this.setVisible(false);
                frame.setVisible(true);
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_memberRegBtnActionPerformed

